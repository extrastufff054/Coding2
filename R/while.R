# While loop in R-programming

# In R, the while loop is used when you want to create a loop that continues until a specific condition becomes false. 
##The while loop repeatedly executes a block of code as long as the given condition remains true. 

# General Syntax : 
    #   while (condition) {
    #       Code to be executed
    #   }

    v <- c ("Hello", "this", "R", "programming")
    x<- 2
    while (x<=6)
    {
        print(v)
        x<-x+1
    }
